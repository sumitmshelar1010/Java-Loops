package MainPackage.JavaBasics.loop;

public class WhileLoop {
    public static void main(String[] args) {
        int i = 1;                                     //Initialization
        while (i <= 5) {                               //Condition
            System.out.println("While MainPackage.JavaBasics.operators.loop executed"); //Statement
            i++;                                       //Increment
        }
    }
}